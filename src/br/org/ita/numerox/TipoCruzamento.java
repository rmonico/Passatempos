package br.org.ita.numerox;

class TipoCruzamento {
	private int tamanhoNumeroFixo;
	private int localCruzamentoNumeroFixo;
	private int tamanhoNumeroTeste;
	private int localCruzamentoNumeroTeste;

	public int getTamanhoNumeroFixo() {
		return tamanhoNumeroFixo;
	}

	public void setTamanhoNumeroFixo(int tamanhoNumeroFixo) {
		this.tamanhoNumeroFixo = tamanhoNumeroFixo;
	}

	public int getLocalCruzamentoNumeroFixo() {
		return localCruzamentoNumeroFixo;
	}

	public void setLocalCruzamentoNumeroFixo(int localCruzamentoNumeroFixo) {
		this.localCruzamentoNumeroFixo = localCruzamentoNumeroFixo;
	}

	public int getTamanhoNumeroTeste() {
		return tamanhoNumeroTeste;
	}

	public void setTamanhoNumeroTeste(int tamanhoNumeroTeste) {
		this.tamanhoNumeroTeste = tamanhoNumeroTeste;
	}

	public int getLocalCruzamentoNumeroTeste() {
		return localCruzamentoNumeroTeste;
	}

	public void setLocalCruzamentoNumeroTeste(int localCruzamentoNumeroTeste) {
		this.localCruzamentoNumeroTeste = localCruzamentoNumeroTeste;
	}

}
